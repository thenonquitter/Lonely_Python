print(len("아 잠 좀 자고 싶다!"))

# 문자열 연결 연산자 : +
print("아 잠 좀 자고 싶다!"[:]+" 오늘도")

# 문자열 반복 연산자 : *
print("아 잠 좀 자고 싶다!"[2:5]*10)

# 문자열 인덱싱 : []
print("아 잠 좀 자고 싶다!"[0])

# 문자열 슬라이싱 : [:]
print("아 잠 좀 자고 싶다!"[:])
print("아 잠 좀 자고 싶다!"[::])
print("아 잠 좀 자고 싶다!"[2:])
print("아 잠 좀 자고 싶다!"[:5])
print("아 잠 좀 자고 싶다!"[::-1])